function storageGet(key) {
  try {
    return window.localStorage.getItem(key);
  } catch {
    return null;
  }
}

function storageSet(key, value) {
  try {
    window.localStorage.setItem(key, value);
    return true;
  } catch {
    return false;
  }
}

function storageRemove(key) {
  try {
    window.localStorage.removeItem(key);
  } catch {
    // Some embedded browsers block storage on file:// pages. Rendering must still work.
  }
}

function escapeAttribute(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/"/g, "&quot;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function getProfile() {
  return {
    name: fields.name.value.trim(),
    phone: fields.phone.value.trim(),
    line: fields.line.value.trim()
  };
}

function contactBlock() {
  const profile = getProfile();
  const name = profile.name || "請填業務姓名";
  const phone = profile.phone || "請填專員電話";
  const line = profile.line || "請填 LINE ID";

  return [
    "普特絲南桃園展示中心",
    "地址：桃園市楊梅區梅獅路二段42號",
    `業務姓名：${name}`,
    `專員電話：${phone}`,
    `LINE ID：${line}`
  ].join("\n");
}

function composeCopy(ad) {
  return [
    ad.title,
    "",
    ...ad.body.flatMap((paragraph) => [paragraph, ""]),
    "歡迎私訊諮詢、預約展示中心體驗或安排丈量。",
    "",
    contactBlock(),
    "",
    ad.tags.join(" ")
  ].join("\n").replace(/\n{3,}/g, "\n\n");
}

function adSearchText(ad) {
  return [
    ad.title,
    ad.subtitle,
    ad.format,
    ...ad.body,
    ...ad.tags
  ].join(" ").toLowerCase();
}

function getFilteredAds() {
  const searchTerm = assetSearch.value.trim().toLowerCase();
  const filterTerms = filterGroups[activeFilter] || [];

  return ads.filter((ad) => {
    const haystack = adSearchText(ad);
    const matchesSearch = !searchTerm || haystack.includes(searchTerm);
    const matchesFilter = activeFilter === "all" || filterTerms.some((term) => haystack.includes(term.toLowerCase()));
    return matchesSearch && matchesFilter;
  });
}

function updateAssetCount(count) {
  assetCount.textContent = count === ads.length ? `共 ${ads.length} 張素材` : `顯示 ${count} / ${ads.length} 張素材`;
  emptyState.hidden = count > 0;
}

