function updatePreviews({ persist = true } = {}) {
  ads.forEach((ad) => {
    const preview = document.querySelector(`[data-preview="${ad.id}"]`);
    if (preview) preview.value = composeCopy(ad);
  });
  updateProfileStatus();
  if (persist) saveProfile();
}

function updateProfileStatus() {
  const profile = getProfile();
  const filled = [profile.name, profile.phone, profile.line].filter(Boolean).length;
  profileStatus.textContent = filled === 3 ? "業務資料已完整套入" : `已填 ${filled} / 3 個欄位`;
}

function saveProfile() {
  storageSet(profileStorageKey, JSON.stringify(getProfile()));
}

function restoreProfile() {
  try {
    const saved = JSON.parse(storageGet(profileStorageKey) || "{}");
    fields.name.value = saved.name || "";
    fields.phone.value = saved.phone || "";
    fields.line.value = saved.line || "";
  } catch {
    storageRemove(profileStorageKey);
  }
}

async function copyText(text) {
  if (navigator.clipboard?.writeText) {
    await navigator.clipboard.writeText(text);
    return;
  }

  const scratch = document.createElement("textarea");
  scratch.value = text;
  scratch.setAttribute("readonly", "");
  scratch.style.position = "fixed";
  scratch.style.opacity = "0";
  document.body.appendChild(scratch);
  scratch.select();
  document.execCommand("copy");
  document.body.removeChild(scratch);
}

function showToast(message) {
  toast.textContent = message;
  toast.classList.add("is-visible");
  window.clearTimeout(showToast.timer);
  showToast.timer = window.setTimeout(() => {
    toast.classList.remove("is-visible");
  }, 2200);
}

function setTemporaryButtonState(button, label) {
  const original = button.innerHTML;
  button.classList.add("is-done");
  button.innerHTML = `${icons.copy}${label}`;
  window.clearTimeout(button.stateTimer);
  button.stateTimer = window.setTimeout(() => {
    button.innerHTML = original;
    button.classList.remove("is-done");
  }, 1500);
}

function openLightbox(ad) {
  lightboxImage.src = ad.image;
  lightboxImage.alt = ad.title;
  lightboxCaption.textContent = ad.title;
  lightbox.hidden = false;
  document.body.classList.add("lightbox-open");
  closeLightbox.focus({ preventScroll: true });
}

function closeImageLightbox() {
  lightbox.hidden = true;
  lightboxImage.removeAttribute("src");
  document.body.classList.remove("lightbox-open");
}

function normalizeUrl(value) {
  const trimmed = value.trim();
  if (!trimmed) return "";
  if (/^https?:\/\//i.test(trimmed)) return trimmed;
  return `https://${trimmed}`;
}

function applyFilters() {
  const filteredAds = getFilteredAds();
  renderAds(filteredAds);
  updatePreviews({ persist: false });
}

function updateBackTopVisibility() {
  backTop.hidden = window.scrollY < 520;
}

function bindEvents() {
  Object.values(fields).forEach((field) => {
    field.addEventListener("input", updatePreviews);
  });

  assetSearch.addEventListener("input", applyFilters);

  filterButtons.forEach((button) => {
    button.addEventListener("click", () => {
      activeFilter = button.dataset.filter;
      filterButtons.forEach((item) => item.classList.toggle("is-active", item === button));
      applyFilters();
    });
  });

  clearProfile.addEventListener("click", () => {
    fields.name.value = "";
    fields.phone.value = "";
    fields.line.value = "";
    updatePreviews();
    showToast("業務資料已清除");
  });

  adGrid.addEventListener("input", (event) => {
    const input = event.target.closest("[data-canva-input]");
    if (!input) return;
    const ad = ads.find((item) => item.id === input.dataset.canvaInput);
    if (!ad) return;
    storageSet(ad.canvaStorageKey, input.value.trim());
  });

  adGrid.addEventListener("click", async (event) => {
    const viewImageButton = event.target.closest("[data-view-image]");
    if (viewImageButton) {
      const ad = ads.find((item) => item.id === viewImageButton.dataset.viewImage);
      if (ad) openLightbox(ad);
      return;
    }

    const copyButton = event.target.closest("[data-copy]");
    if (copyButton) {
      const ad = ads.find((item) => item.id === copyButton.dataset.copy);
      if (!ad) return;
      try {
        await copyText(composeCopy(ad));
        setTemporaryButtonState(copyButton, "已複製");
        showToast(`已複製：${ad.title}`);
      } catch {
        showToast("複製失敗，請手動選取內容");
      }
      return;
    }

    const canvaButton = event.target.closest("[data-open-canva]");
    if (canvaButton) {
      const ad = ads.find((item) => item.id === canvaButton.dataset.openCanva);
      const input = document.querySelector(`[data-canva-input="${canvaButton.dataset.openCanva}"]`);
      const url = normalizeUrl(input?.value || "");
      if (!ad || !url) {
        input?.focus();
        showToast("請先貼上 Canva 編輯網址");
        return;
      }

      try {
        const parsed = new URL(url);
        storageSet(ad.canvaStorageKey, parsed.href);
        if (input) input.value = parsed.href;
        window.open(parsed.href, "_blank", "noopener,noreferrer");
      } catch {
        input?.focus();
        showToast("Canva 連結格式需要是網址");
      }
    }
  });

  closeLightbox.addEventListener("click", closeImageLightbox);

  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox || event.target.classList.contains("image-lightbox__stage")) {
      closeImageLightbox();
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && !lightbox.hidden) {
      closeImageLightbox();
    }
  });

  backTop.addEventListener("click", () => {
    window.scrollTo({ top: 0, behavior: "smooth" });
  });

  window.addEventListener("scroll", updateBackTopVisibility, { passive: true });
}

restoreProfile();
renderAds();
bindEvents();
updatePreviews();
updateBackTopVisibility();
